#1.-Ejemplo de toma de desciones
mascota = 'perro'

if mascota == 'gatp':
    print('Tienes un gato')
elif mascota == 'perro':
    print('Tienes un perro')
elif mascota == 'pez':
    print('Tienes un pez')
else:
    print('No se que animal tienes')

