#RANGO estableces un rango de numero sin establecer una lista
for numero in range(5):
    print(numero)

#Establecer el rango
for numero in range(2,5):
    print(numero)

#Establecer el ragno y cada cuanto
for numero in range(2,10,2):
    print(numero)

#Generar listas con range sin loop
lista = list(range(1,101))
print(lista)